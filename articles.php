<h1 style="text-align:center"><b>BE INFORMED!!!</b></h1>
<h3 style="text-align:center"><b><i>Articles that every Indian must know</i></b></h3>
<br>

<p style="font-size:150%" align="center" color="black"><b><i>
* POCSO Act: The Protection of Children from Sexual Offences Act (POCSO Act) 2012 was established to protect the children against<br>
             offences like sexual abuse, sexual harassment and pornography. It was formed to provide a child-friendly system for<br>
			 trial underneath which the perpetrators could be punished.<br><br>
			 
* Motor Vehicle Act 1988, section -185 202: At the time of driving if your 100ml. blood contains more than 30mg. of alcohol then<br>
                                            police can arrest you without a warrant.<br><br>
		
* Criminal Procedure Code, Section 46: No woman cannot be arrested before 6 A.M. and after 6 P.M.<br><br>

* Indian Penal Code, 166 A: A Police officer can’t refuse to lodge an FIR if he/she does so they could be jailed for up to<br>
                            6 months to 1 year.<br><br>
							
* Indian Srius Act, 1887: Even any 5 star hotel can’t prohibit you from drinking potable water and using its washrooms.<br><br>

* Motor Vehicle Act, 1988: As per the Section 129 of the Indian Motor Vehicle Act, wearing the helmet is must for two wheeler riders.<br>
                           Section 128 of this Motor Vehicle Act limits maximum two riders on the bikes. This law also says that if <br>
						   the traffic police officer snatches the key from the car or motorcycle, it is illegal. You have the full <br>
						   right to launch a Legal proceeding against the officer.<br><br>
						   
* Domestic Violence Act, 2005: If a young boy and a girl want to live together in “live in relationship” ,they can do so because it is<br>
                               not illegal. Even the new born from this relationship is also a legal son or daughter and this new born <br>
							   have the full right in the assets of his/her father.<br><br>

* Police Act, 1861: A police officer is always on duty whether he/she wearing a uniform or not. If a person makes a complaint to the Vehicle<br>
                    officer, he/she could not say that he can’t help the victim because he/ she is not on the duty.<br><br>
					
* Maternity Benefit Act, 1961: No company can fire a pregnant woman. It may be punishable by a maximum of 3 years of imprisonment.<br><br>

* Income Tax Act, 1961: In the case of tax violations, the tax collection officer has the power to arrest you but before arresting you<br>
                        he/she will have to send a notice to you. Only Tax Commissioner decides that how long you will stay in the custody.<br><br>
						
* Hindu Marriage Act, Section -13: As per the Hindu Marriage Act, 1955 (any husband or wife) may apply for divorce in the court on the<br> 
                                   basis of Adultery (physical relationship outside of marriage), physical and mental abuse, impotency,<br> 
								   to leave home without information, to change Hindu religion and adopt other religion, insanity, incurable<br>
								   disease and no information about husband or wife for seven years.<br><br>
								   
* Code of Criminal Procedure, 1973: Only women police constable can arrest to women.Male constable doesn’t have the right to arrest women.<br> 
                                    Women have the right to deny going to police stations after the 6 P.M. and before the 6 A.M. In the case <br>
									of a serious crime only after receipt of the written order from the magistrate a male policeman can arrest<br>
									a woman.<br><br>
									
* Foreign Contribution Regulation Act (FCRA), 2010: It would surprise you to know that if you take a gift from any company on the occasion of a<br>
                                                    festival, it falls into the category of bribery. You can also be sentenced to jail for this <br>
													crime.<br><br>
													
* As per the Citizen Charter: If their gas cylinder blasts during the cooking of food then the gas agency is liable to pay Rs. 50 lakh to the <br>
                              victim as a compensation. To claim this compensation consumer need to logde an FIR to the nearest police station<br>
							  and submit it to the concerned gas agency.<br><br>
							  
* Automotive (Amendment) Bill, 2016: If you are fined for a crime (like riding without a helmet or any other reason) then you will not be fined <br>
                                     for the same reason in the same day.<br><br>
								
* Maximum Retail Price Act, 2014: Any Shop keeper can’t charge more than the printed price of any commodity but a consumer has the right to bargain<br> 
                                  for less than the printed price of a commodity.			<br><br>

* Limitation Act, 1963:  If your office does not pay you then you have the power to file an FIR against it within 3 years. But if you report after<br>
                         3 years, you will not get anything for the due.		<br><br>						  
						 
* Section 294 of the Indian Penal Code: If you are found involved in “obscene activity" at a public place, you can be imprisoned for 3 months.<br><br>

* Hindu Adoption and Maintenance Act: If somebody belongs to Hindu religion and  has a son or grandson then he can’t adopt a second child.<br>
                                      There must be a gap of at least 21 years between you (the adopter) and your adopted son.<br><br>

* Delhi Rent Control Act, 1958, Section 14: If you are living in Delhi then your land lord does not have the right to forcefully vacate your house<br>
                                            without giving prior notice to you.									  
</i></b></p>
